// Function to find the node with the minimum distance value
uint8_t minDistance(uint8_t dist[], bool visited[], uint8_t numNodes)
{
    uint8_t min = 255;
    uint8_t minIndex = 0;

    for (uint8_t v = 0; v < numNodes; v++)
    {
        if (visited[v] == false && dist[v] <= min)
        {
            min = dist[v];
            minIndex = v;
        }
    }

    return minIndex;
}

